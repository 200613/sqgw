package com.example.demo.pojo;

public class Cform {

	
	private Integer id;
	
	private String order_id;
	
	private String form_name;
	
	private String form_logo;
	
	private String form_unitprice;
	
	private Integer form_count;
	
	private Integer form_goodid;


	public Integer getId() {
		return id;
	}

	public void setId(Integer id) {
		this.id = id;
	}

	public String getOrder_id() {
		return order_id;
	}

	public void setOrder_id(String order_id) {
		this.order_id = order_id;
	}

	public String getForm_name() {
		return form_name;
	}

	public void setForm_name(String form_name) {
		this.form_name = form_name;
	}

	public String getForm_logo() {
		return form_logo;
	}

	public void setForm_logo(String form_logo) {
		this.form_logo = form_logo;
	}

	public String getForm_unitprice() {
		return form_unitprice;
	}

	public void setForm_unitprice(String form_unitprice) {
		this.form_unitprice = form_unitprice;
	}

	

	public Integer getForm_count() {
		return form_count;
	}

	public void setForm_count(Integer form_count) {
		this.form_count = form_count;
	}

	public Integer getForm_goodid() {
		return form_goodid;
	}

	public void setForm_goodid(Integer form_goodid) {
		this.form_goodid = form_goodid;
	}


	
}
